#!/bin/bash
# Generate Mermaid class diagrams for cpp_accelerator using clang-uml.
#
# Usage:
#   scripts/build/uml.sh                         # all diagrams
#   scripts/build/uml.sh --diagram cpp_domain_layer   # single diagram
#   scripts/build/uml.sh --refresh-db            # refresh compile_commands first
#
# Output: docs/uml/generated/<diagram-name>.mmd
#
# One-time setup:
#   scripts/dev/install-clang-uml.sh
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"

# ── Parse arguments ────────────────────────────────────────────────────────
DIAGRAM_FILTER=""
REFRESH_DB=false

while [[ $# -gt 0 ]]; do
    case "$1" in
        --diagram)
            DIAGRAM_FILTER="$2"
            shift 2
            ;;
        --refresh-db)
            REFRESH_DB=true
            shift
            ;;
        -h|--help)
            sed -n '2,10p' "$0"
            exit 0
            ;;
        *)
            echo "Unknown option: $1"
            exit 1
            ;;
    esac
done

cd "$PROJECT_ROOT"

# ── Prerequisites check ────────────────────────────────────────────────────
if ! command -v clang-uml &>/dev/null; then
    echo "ERROR: clang-uml not found."
    echo "       Run: scripts/dev/install-clang-uml.sh"
    exit 1
fi

if [[ ! -f compile_commands.json ]]; then
    echo "compile_commands.json not found — refreshing now..."
    REFRESH_DB=true
fi

if $REFRESH_DB; then
    echo "Refreshing compile_commands.json..."
    bazel run @hedron_compile_commands//:refresh_all
fi

# ── Generate diagrams ──────────────────────────────────────────────────────
mkdir -p docs/uml/generated

CLANG_UML_ARGS=(--generator mermaid)

if [[ -n "$DIAGRAM_FILTER" ]]; then
    CLANG_UML_ARGS+=(-n "$DIAGRAM_FILTER")
    echo "Generating diagram: $DIAGRAM_FILTER"
else
    echo "Generating all diagrams..."
fi

clang-uml "${CLANG_UML_ARGS[@]}"

# ── Rebuild index.md by embedding all .mmd files ──────────────────────────
if [[ -z "$DIAGRAM_FILTER" ]]; then
    INDEX="docs/uml/index.md"
    {
        cat << 'EOF'
# UML Class Diagrams — cpp_accelerator

> Auto-generated by [`scripts/build/uml.sh`](../../scripts/build/uml.sh) using
> [clang-uml](https://github.com/bkryza/clang-uml).
> Open this file in VS Code and press `Ctrl+Shift+V` to view all diagrams
> (requires the [bierner.markdown-mermaid](https://marketplace.visualstudio.com/items?itemName=bierner.markdown-mermaid) extension).

---

## 1. Domain Layer

Pure abstractions — no infrastructure dependencies.

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_domain_layer.mmd
        echo '```'

        cat << 'EOF'

---

## 2. Application Layer

Engine, Pipeline, and Filter Factory Registry.

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_application_layer.mmd
        echo '```'

        cat << 'EOF'

---

## 3. Ports

Hexagonal port interfaces (control and media).

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_ports.mmd
        echo '```'

        cat << 'EOF'

---

## 4. Core Utilities

Logger, Telemetry, Result, SignalHandler.

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_core.mmd
        echo '```'

        cat << 'EOF'

---

## 5. Control & Media Adapters

gRPC outbound client and WebRTC (manager, signaling, data channels).

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_control_adapters.mmd
        echo '```'

        cat << 'EOF'

---

## 6. Compute Backend — CPU

CPU filter implementations.

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_compute_cpu.mmd
        echo '```'

        cat << 'EOF'

---

## 7. Compute Backend — CUDA

CUDA filters, memory pool, and TensorRT YOLO inference.

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_compute_cuda.mmd
        echo '```'

        cat << 'EOF'

---

## 8. Compute Backend — OpenCL

OpenCL filter implementations.

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_compute_opencl.mmd
        echo '```'

        cat << 'EOF'

---

## 9. Compute Backend — Vulkan

Vulkan filter implementations.

EOF
        echo '```mermaid'
        cat docs/uml/generated/cpp_compute_vulkan.mmd
        echo '```'

    } > "$INDEX"
    echo "  index.md rebuilt  ($(wc -l < "$INDEX") lines)"
fi

# ── Summary ────────────────────────────────────────────────────────────────
echo ""
echo "Generated diagrams in docs/uml/generated/:"
for f in docs/uml/generated/*.mmd; do
    [[ -f "$f" ]] || continue
    lines=$(wc -l < "$f")
    echo "  $(basename "$f")  (${lines} lines)"
done
echo ""
echo "Open docs/uml/index.md in VS Code and press Ctrl+Shift+V to view all diagrams."
echo "Or paste individual .mmd files at https://mermaid.live"
